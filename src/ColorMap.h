inline Color operator+(const Color& c0, const Color& c1)
{
	return Color(c0[0] + c1[0], c0[1] + c1[1], c0[2] + c1[2]);
}

inline Color operator*(double s, const Color& c)
{
	return Color(s * c[0], s * c[1], s * c[2] );
}

inline Color GetColor(double x, ColormapType type)
{
	switch (type)
	{
	case ColormapType::Heat:
		return GetHeatColor(x);
	case ColormapType::Jet:
		return GetJetColor(x);
	case ColormapType::Hot:
		return GetHotColor(x);
	case ColormapType::Gray:
		return GetGrayColor(x);
	case ColormapType::Magma:
		return GetMagmaColor(x);
	case ColormapType::Inferno:
		return GetInfernoColor(x);
	case ColormapType::Plasma:
		return GetPlasmaColor(x);
	case ColormapType::Viridis:
		return GetViridisColor(x);
	case ColormapType::Github:
		return GetGithubColor(x);
	default:
		break;
	}
}

inline Color GetHeatColor(double x)
{
	x = std::max(0.0, std::min(1.0, x));

	Color data[] =
	{
		Color(0.0, 0.0, 1.0),
		Color(0.0, 1.0, 1.0),
		Color(0.0, 1.0, 0.0),
		Color(1.0, 1.0, 0.0),
		Color(1.0, 0.0, 0.0)
	};

	const double a  = x * ((sizeof(data) / sizeof(Color)) - 1);
	const double t  = a - std::floor(a);
	const Color  c0 = data[static_cast<size_t>(std::floor(a))];
	const Color  c1 = data[static_cast<size_t>(std::ceil (a))];

	return (1.0 - t) * c0 + t * c1;
}

inline Color GetJetColor(double x)
{
	x = std::max(0.0, std::min(1.0, x));

	Color data[] =
	{
		Color(0.0, 0.0, 0.5),
		Color(0.0, 0.0, 1.0),
		Color(0.0, 0.5, 1.0),
		Color(0.0, 1.0, 1.0),
		Color(0.5, 1.0, 0.5),
		Color(1.0, 1.0, 0.0),
		Color(1.0, 0.5, 0.0),
		Color(1.0, 0.0, 0.0),
		Color(0.5, 0.0, 0.0)
	};

	const double a  = x * ((sizeof(data) / sizeof(Color)) - 1);
	const double t  = a - std::floor(a);
	const Color  c0 = data[static_cast<size_t>(std::floor(a))];
	const Color  c1 = data[static_cast<size_t>(std::ceil (a))];

	return (1.0 - t) * c0 + t * c1;
}

inline Color GetHotColor(double x)
{
	x = std::max(0.0, std::min(1.0, x));

	Color r(1.0, 0.0, 0.0);
	Color g(0.0, 1.0, 0.0);
	Color b(0.0, 0.0, 1.0);

	if (x < 0.4)
	{
		const double t = x / 0.4;
		return t * r;
	}
	else if (x < 0.8)
	{
		const double t = (x - 0.4) / (0.8 - 0.4);
		return r + t * g;
	}
	else
	{
		const double t = (x - 0.8) / (1.0 - 0.8);
		return r + g + t * b;
	}
}

inline Color GetGrayColor(double x)
{
	x = std::max(0.0, std::min(1.0, x));

	return (1.0 - x) * Color(1.0, 1.0, 1.0);
}

